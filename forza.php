<strong>Acerca de este juego</strong>
<br> <br> 
En este juego, puedes explorar un mapa de mundo abierto verdaderamente grande, hasta un cincuenta por 
ciento más grande que el nada despreciable mapa de Forza Horizon 4. Corre con una variedad de vehículos a
través de los volcanes, explora selvas y junglas tropicales en busca de sus ciudades escondidas en ruinas, 
tira arena mientras te acercas a las playas y pasa corriendo por cascadas, montañas nevadas y grandes ciudades
basadas en las reales, como Guanajuato, que incluye una red de túneles secretos para explorar.
<br> <br> 
México es un país vasto y tiene climas increíblemente diversos dependiendo de en qué parte del país te 
encuentres y los desarrolladores del juego se han esforzado por brindarte la experiencia climática 
mexicana completa en este juego. Las cuatro estaciones se comportan de manera diferente en cada uno de 
los once biomas diversos y al igual que en la vida real, conduces a través de eventos climáticos: llueve 
en la montaña y luego conduces hacia el sol abrasador en el desierto.
<br>
Todos los vehículos y varios personajes del juego se pueden personalizar ampliamente para que puedas 
verte exactamente de la manera que desees y también conducir el automóvil de tus sueños. Juega para competir 
contra tus rivales o simplemente recorre libremente los escenarios y alucina con lo que te puedes encontrar: 
todos los elementos del juego aparecen para los exploradores, simplemente lo hacen de una manera más aleatoria 
y sorprendentemente naturalista.
