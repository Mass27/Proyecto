<strong>Acerca de este juego</strong>
<br> <br> 
 La historia general de la serie sigue a una banda de personajes que se resisten al 
 inexorable avance de la Horda Locust - humanoides reptilianos subterráneos, quienes 
 esperaron hasta que la civilización humana se debilitara y se desmolarizara tras una 
 guerra civil prolongada y aparentemente infranqueable para arremeter en contra de sus 
 fatigados y diezmados ejércitos, a los que superaron con facilidad.
<br> <br>
La línea temporal de la serie establece que, si bien la mayoría de los primeros juegos 
se desarrollaban desde la perspectiva de un mismo personaje, los posteriores tienden a
saltar de una a otra, explorando distintos puntos de vista y ofreciendo interpretaciones 
cambiantes acerca de elementos de la historia en los que hasta ahora no se había enfatizado, 
añadiendo historias de trasfondo para conformar un rico tramado narrativ
<br><br>
<strong>Novedades de Gear of war</strong>
    
 
<br> <br>
<ul>
<li>
Esta entrega se caracteriza por contar con el terreno de juego más grande y completo 
de la serie, cubriendo una gran variedad de paisajes que van desde los desiertos hasta
 los glaciares, entre muchos otros - todos ellos maravillosamente realizados y lo 
 suficientemente detallados como para que puedas confundirlos fácilmente con imágenes 
 de lugares reales
<br> <br>
</li>
<br> 
    
<li>
Puedes elegir entre varias opciones de juego. Puedes jugar en solitario o en cooperativo 
con un amigo, o puedes probar el modo multijugador en línea, que permite que hasta ocho
 jugadores jueguen en conjunto. O bien, puedes probar el nuevo modo cooperativo para tres 
 jugadores, llamado Escape, en el que tú y dos amigos deben colocar un explosivo en una 
 fortaleza enemiga y luego escapar a salvo antes de que éste se detone
<br> <br>
</li>
<br> 
<li>
El juego está catalogado como un AAA, es decir, una mega producción de alto presupuesto, el
equivalente en videojuegos a una película que ha sido diseñada para ser un éxito en taquilla, con todo lo que esto conlleva: acción por montones, un gran presupuesto para su desarrollo, y una jugabilidad entretenida y amigable con el jugador 
<br> <br>

</li>
<br> 

<li>
Hay muchas funciones de accesibilidad para asegurar que todos los jugadores puedan disfrutar
de una gran experiencia de juego, sin importar su nivel de habilidades
    <br> <br>
</li>
<br> <br>
  </ul>


