<div class="description">

<span class="hide" id="htex">
En este nuevo juego de la franquicia Battlefield, el clima del mundo ha evolucionado hacia los extremos, lo que ha provocado que un gran número de personas huyan de sus hogares ahora insostenibles. Estos refugiados climáticos son llamados No-Pats, lo que significa que son apátridas y "no tienen país". La crisis climática ha empeorado drásticamente, provocando el colapso de la UE y aumentando las tensiones entre Estados Unidos y Rusia hasta casi la guerra. 
<br> <br> 
    
Los eventos en la Tierra llegan a un punto crítico cuando ocurre un evento del Síndrome de Kessler * en 2040, que destruye el 70% de los satélites de comunicaciones internacionales. Ya tensos, Estados Unidos y Rusia están al borde de la guerra.
<br> <br> 
Un evento del Síndrome de Kessler ocurrirá, según la ciencia, cuando una reacción en cadena de colisiones dentro del grueso cinturón de basura espacial que hay alrededor del planeta (escombros de misiones espaciales, perdidos o desechados por cualquier motivo) afecte a los elementos en órbita en los que confiamos para trabajar y para divertirnos día a día, es decir, los satélites cuidadosamente colocados que operan los sistemas de teléfono, las conexiones a Internet, así como las comunicaciones por radio y televisión.

<br> <br> 
<b><strong>Que hay nuevo y que se mantuvo igual?</strong></b>
<br> <br> 
El sistema Plus es nuevo y mejorado: los jugadores ahora pueden actualizar sus armas sobre la marcha, 
y mientras el sistema de clases es básicamente el mismo, pero con una revisión masiva y muy necesaria. 
Las clases ahora se llaman "especialidades" y puedes explorar cualquier árbol de habilidades, 
 en lugar de estar limitado a tu clase como antes. Los nombres siguen siendo los mismos, Asalto, Apoyo, Médico, Reconocimiento, etc., por lo que si tienes un favorito, puedes encontrar rápidamente lo que estás buscando.
<br> <br>
Los cambios climáticos extremos han dado el salto con respecto al juego anterior de la serie, y dado que 
hay una emergencia climática, no te sorprenderá saber que los tornados pueden atraparte y hacerte pasar 
un mal rato antes de escupirte de nuevo, con suerte, mientras que las tormentas de arena reducen enormemente 
tu visibilidad. Y eso puede ser un problema en medio de un campo de batalla, la verdad.
<br> <br>
En lugar del modo para un jugador hay tres modos de juego principales: Guerra Total, 
que se compone de: Avance (en el que intentas eliminar los puntos de control del otro equipo) 
y Conquista (en el que dos equipos luchan por un solo control punto). Zona de Peligro es el que 
ofrece una versión moderna de la experiencia multijugador. Hay soporte para hasta 128 jugadores en dos equipos de 64.
