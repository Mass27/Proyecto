<strong>Acerca de este juego</strong>
<br> <br> 
¡El Jefe Maestro está de vuelta! Siguiendo a los eventos de Halo 5, este juego retoma la historia de Master 
Chief, su protagonista principal, y esta vez lo transporta al pasado dotándolo de una nueva armadura que es 
un guiño nostálgico a sus primeras apariciones y a las entregas anteriores de la serie.
<br> <br> 
Halo 4 dio inició a una trilogía, la cual es conocida como la Saga del Reclamador, y esta nueva entrega 
es el episodio final de dicha trilogía. Los eventos de Halo Wars 2 también son relevantes, entramándose 
en la historia y proporcionando algunos puntos de interés y detalles argumentales que contribuyen a que 
el juego sea una experiencia sólida e inmersiva.
<br> <br>
<strong>Modos de Juegos</strong>
<br> <br>
<ul> 
<li>
Un jugador– sigue el arco de la historia principal, eliminando enemigos, aprendiendo más acerca del Jefe 
Maestro y de los amigos a medida que progresas para llegar a la conclusión de la misma
<br> <br>
</li>
<br> <br>
<li>
Multijugador – juega con amigos o en contra de desconocidos, con un máximo de tres jugadores más. 
El juego se mostrará con la pantalla dividida en cuatro partes para que puedas estar al tanto de 
las acciones de los otros jugadores y cuides de tu propia espalda. 
<br> <br>
</li>
<li>
¡El Modo Forja está de regreso! Similar a una función de ‘creación de mundos’, esta modalidad diseñada 
para crear nuevas áreas te permite personalizar mapas y ajustar tu experiencia de juego. Los desarrolladores 
han quedado tan impresionados por las creaciones que los jugadores han hecho en el Modo Forja que ahora han 
incluido una mayor cantidad de contenido, ¡con nuevos recursos de creación y herramientas de edición por 
montones! 
<br><br>
</li>
<li>
¿Modo Battle Royale? – El juego NO cuenta con una modalidad de Battle Royale, pero los desarrolladores han 
hecho hincapié en el de hecho de que, si los jugadores son lo suficientemente entusiastas y decididos, 
podrían crear arenas al estilo Battle Royale utilizando el Modo Forja, si se establecen las reglas de 
juego apropiadas… 
</li>
</ul>
