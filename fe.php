<strong>Acerca de este juego</strong>
    <br> <br> 
Aquí, el orden lo mantiene la Iglesia de Seiros, que alberga la prestigiosa Academia de Oficiales dentro de su sede. Estás invitado
a enseñar una de sus tres poderosas casas, cada una compuesta por estudiantes rebosantes de personalidad y representada por un
miembro de la realeza de uno de los tres territorios. Como su profesor, debe guiar a sus alumnos en sus vidas académicas y en 
batallas tácticas de rol por turnos forjadas con nuevos giros estratégicos para superar. ¿Qué casa y qué camino elegirás?
<br> <br> 
El juego presenta la jugabilidad refinada por la que es conocida la franquicia Fire Emblem™. Dirige a un grupo de guerreros 
para que se muevan y luchen en un campo de batalla basado en cuadrículas y, por primera vez en la historia de la serie, asigna 
batallones de tropas para apoyar a las unidades individuales en la batalla. Como profesor, usted es responsable de enseñar a sus 
alumnos y mejorar sus habilidades en la vida académica y en la batalla. Estas pueden ser tareas escolares, pero lo que está en juego 
es muy real. La vida de sus estudiantes depende de su liderazgo. Depende de ti guiar a cada uno de ellos, para que puedan manejar una 
variedad de armas, dominar el estudio de la magia y adquirir habilidades especiales como la equitación. Pero hay más en ser profesor 
que comandar ejércitos. Recorra libremente el Monasterio Garreg Mach y la academia dentro de él, mientras interactúa con estudiantes 
talentosos para construir relaciones y recopilar información. Después de conocer a una chica enigmática llamada Sothis... te darás
 cuenta de que solo aparece en tu mente. ¿Qué otros misterios aguardan? 
