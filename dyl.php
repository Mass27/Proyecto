<li>
 Armas cuerpo a cuerpo: diseñadas para un uso personal y cercano, las armas cuerpo a cuerpo son las más 
 utilizadas en este juego. ¡Sin embargo, asegúrate de vigilar tus armas ya que estas se degradan a medida 
 que las usas y tu práctica daga podría romperse de repente justo cuando estás a punto de administrar el 
 golpe de gracia!  
<br> <br>
</li>
