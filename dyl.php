<strong>Acerca de este juego</strong>
<br> <br> 
El parkour sigue siendo una característica básica de este título, igual que lo es el combate, y 
Aiden Caldwell tiene una gran cantidad de nuevas tecnologías para ayudarlo con sus tareas. De hecho, 
el parkour ha mejorado enormemente, con hasta el doble de movimientos nuevos que antes. El movimiento 
de Aiden por la ciudad refuerza con la adición de un gancho de agarre para que pueda subir esos edificios 
más altos y un parapente para movimientos laterales rápidos y libres.
<br> <br> 
Y el movimiento lateral es más importante en este juego, con mucha más ciudad para que Aiden se mueva 
libremente. El mapa en esta secuela es cuatro veces más grande que el del juego original y todo él es 
un sandbox abierto, por lo que puedes explorar por todas partes y descubrir todo tipo de divertidos 
espacios ocultos que pueden o no contener botín u otros tesoros que harán la vida de tu personaje un 
poco más fácil.
<br>
<strong>Armas Y Movilidad</strong>
<br> <br>
<ul class="bb"> 
<li>
 Parkour: movimientos elegantes, parte baile, parte performance artística urbana y parte de combate. 
 ¡Tu personaje usará las características urbanas a su alrededor para moverse por encima, por debajo o por 
 al lado de las hordas de zombis. De vez en cuando, necesitará utilizar sus habilidades para liberarse
 de una de esas hordas, si es que le rodean o se acercan más de lo deseado. Con muchos movimientos antiguos, 
 así como todos los nuevos para aprender, invierte algo de tiempo y esfuerzo en dominar esta habilidad: 
 ¡Nunca sabes cuándo te salvará el trasero!
<br> <br>
</li>
