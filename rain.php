<div class="description">
    <strong>Acerca de este juego</strong>
    <br> <br> 
   
 Cada infiltración, conocida en el juego como incursión, involucra tres áreas de mapa interconectadas y le pide al jugador que recopile o complete cualquiera de las doce misiones incluidas en cada sector del mapa. Puedes solicitar la extracción después de cada misión exitosa, o continuar en la siguiente sección, obteniendo recompensas más altas por esta última. Sin embargo, si te matan o te capturan durante las últimas misiones, perderás todo tu botín, lo que puede ser un poco decepcionante.
<br><br>
Si un miembro de tu equipo es capturado por el enemigo, aparecerá en el tablero como MIA y ese personaje no se podrá jugar hasta que sea rescatado. Del mismo modo, si se lesionan gravemente, deberán tomarse un tiempo para recuperarse y se necesitará más tiempo dependiendo de la gravedad de la lesión.
<br><br>
Los enemigos son extraterrestres que se llaman Arqueas y no te quepa ninguna duda de que estás jugando en su patio trasero en este juego. Son más fuertes en un terreno mutado llamado Sprawl, que se parece mucho a una gruesa mancha de aceite móvil y que te ralentiza a ti y a tu equipo.
Pero no tienes que pisarlo, afortunadamente, disparar el Sprawl hace que se expanda fuera de tu camino, dejándote vía libre, temporalmente, al menos.
<br><br>
El ritmo se mantiene deliberadamente lento y cuidadoso, con pocos recursos (comida, paquetes de ayuda médica y combustible) para que puedas volver a tener una salud completa y una curación inexistente durante y entre misiones. Esto genera tensión y hace que el juego sea aún más interesante de lo que sería de otra manera. Adquiere el hábito de usar el modo sigiloso como estándar, ya que el ruido excesivo atraerá a los enemigos, cuyos gritos de alarma convocarán a muchos más Arqueas.
<br><br>
Se espera que tu equipo use la astucia para emboscar al enemigo en grupos pequeños (el modo sigiloso mencionado anteriormente) en lugar de esperar detrás de las fortificaciones para esperar a que vengan a ti. El ecosistema es más amable con los extraterrestres que contigo (como lo demuestra el Sprawl) y están en constante evolución: rápidamente descubrirás que los Arqueas tienen diez formas y tamaños diferentes (ver más abajo para más información).

    <br>
    <strong>Neceistas Saber</strong>
    <br><br>
    Hay un menú de operadores completo en el juego y puedes configurar y restablecer las habilidades de tu personaje antes de cada misión, eligiendo entre 18 operadores diferentes. Algunos de estos personajes serán reconocibles para los jugadores de los títulos anteriores de la franquicia. Cada personaje viene con sus propias habilidades y armas únicas.
    <br><br>
    En otras partes del juego, puedes enviar drones para reconocimiento, disparar a través de las paredes para eliminar a los enemigos que se esconden y reforzar tus propias puertas y ventanas para evitar la fuga de los enemigos capturados. Puedes enviar información a tus compañeros de equipo sobre las posiciones enemigas y los recursos que tienen.
    <br><br>
    Además del modo cooperativo de tres personas, también puedes jugar solo, abriéndote camino a través de los doce mapas del juego. Este juego se llamó primero Quarantine, luego Parasite, antes de decidirse por Extraction poco antes del lanzamiento.
 
