<strong>Acerca de este juego</strong>
<br> <br> 
La premisa del juego es ridículamente simple: comienzas tu aventura con nada más que tus manos y un 
inventario limitado. A partir de esto, debes comenzar a minas para reunir materiales que luego utilizarás 
para fabricar herramientas y construir casas y otros edificios.
<br> <br> 
Los recursos que puedes encontrar van desde los más comunes hasta los súper raros, y a menudo los productos 
más raros y valiosos son los más difíciles de conseguir, requiriendo de minería profunda e incluso de la 
construcción de túneles reforzados para evitar que desbordamientos de agua o deslaves de rocas te retrases.
<br>
No hay ninguna historia a seguir: el desarrollo del juego depende completamente de ti y de lo que desees 
lograr. Puedes optar por construir ciudades, o una mansión rural, puedes criar ovejas o ir de pesca. El 
juego es exactamente lo que tú y tu imaginación quieren que sea. 
