<span class="hide" id="htex">
<br><br>
<strong>Acerca de este juego</strong>
<br><br>
La trama del juego se desarrolla en el contexto de los dioses nórdicos en esta iteración, 
en lugar de la Antigua Grecia de los juegos originales, y se desarrolla libremente en la 
Escandinavia medieval en el reino de Midgard, que es el equivalente nórdico de las tierras
habitadas por los primeros humanos cuando el mundo aún era joven.
<br> <br> 
Hay dos protagonistas en el juego, uno de los cuales es jugable. Kratos - un ex dios griego 
de la guerra, va acompañado por su hijo pequeño, Atreus, que parece tener entre ocho y doce 
años más o menos. Los dos se embarcan en una misión de honor: deshacerse de las cenizas de 
Faye (la madre de Atreus) de acuerdo con su último deseo en el lecho de muerte, para que sus 
restos sean esparcidos en el pico más alto de los nueve reinos.
<br> <br> 
Por supuesto, no habría historia si todo saliera bien, por lo que su búsqueda se ve 
interrumpida por monstruos y dioses que tienen otras ideas sobre quién debería poder 
deshacerse de sus restos terrenales en la cima sagrada. Los monstruos comienzan siendo 
grandes y aterradores y se vuelven cada vez más grandes y aterradores, lo que te brinda un 
amplio margen para mejorar tus habilidades y luego probarlas. El combate es maravilloso: 
sangriento, brutal y, gracias a las físicas, satisfactorio.
<br> <br> 
Casi igualmente satisfactorio y raro en un juego más conocido por los crujidos que rompen 
el cráneo y los huesos rotos, es el arco argumental entre Kratos y Atreus. Cuando parten, 
son casi extraños el uno para el otro, ambos sienten que el otro espera más de ellos de lo 
que dan. Este arco avanza junto con el juego, terminando con una relación mucho más cercana, 
respetuosa y amorosa entre los dos. ¡Y todo sin impactar en el derramamiento de sangre que se
produce durante todo el juego!
<br> <br> 
<strong>CARACTERÍSTICAS ClAVES</strong>
<br> <br>
Tómate un momento antes de jugar para personalizar la configuración de tu PC y adaptarla a 
tu CPU para obtener los mejores resultados posibles. Pensarás que vale la pena el tiempo, 
cuando veas el hermoso paisaje que hace que incluso las partes tranquilas sean una experiencia maravillosa. Durante las secuencias de acción, estarás demasiado ocupado para prestar atención a tu entorno, a menos que encuentres una cueva práctica para esconderte o una roca para refugiarte mientras recuperas el aliento.
<br> <br>
<ul>
<strong>Que armas se encuentran?</strong>
<li>
Hacha Leviatán: el hacha de Kratos es algo así como el martillo de Thor, el Mjolnir, en el 
sentido de que regresa cuando se le invoca. Programa tus lanzamientos cuidadosamente, para 
que puedas conseguir dos enemigos por el precio de uno: uno al salir y el otro al regresar. 
El Hacha Leviatán se puede mejorar, así que sigue buscando refuerzos.
<br> <br>
</li>
<li>
Arco de Garra: el arco y la flecha de Atreus siempre están con él, y son útiles cuando te 
acompaña en tus batallas, eliminando enemigos mientras estás ocupado.   
<br> <br>
</li>
<li>
Hand combat: el modo "Ira Espartana" de Kratos le da un poder de súper puñetazo que lleva un 
simple puñetazo (aunque ya es muy poderoso) a otra dimensión.
<br><br>
</li>
<li>
Escudo del Guardián: el guantelete de Kratos guarda un secreto. El escudo de tamaño 
completo aparece y evita flechas, puños desprevenidos y lanzas, siempre y cuando lo 
despliegues a tiempo, claro.
<br> <br>
</li>
<li>
nvocación Rúnica Ira del Lobo – Atreus tiene varios ataques rúnicos que piden ayuda cuando 
la necesita. Como un lobo espectral gigante que ataca a los enemigos tres veces antes de 
desaparecer de nuevo.
<br><br>
</li>
<li>
Armadura: todo lo que usa Kratos se puede personalizar y eso marca la diferencia. Ten cuidado 
con hacer que tu armadura sea demasiado gruesa, ya que esto puede ralentizarte, por ejemplo. 
Con cada actualización, tendrás que sopesar varias estadísticas: defensa sobre agilidad, por 
ejemplo, así que piensa en cada opción y elige sabiamente.
<br> <br>
</li>
</ul>
