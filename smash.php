<strong>Acerca de este juego</strong>
<br> <br> 
Igual que sus predecesores, se trata de un juego de lucha donde hay que debilitar a los rivales para expulsarles de los 
márgenes del escenario. El plantel está formado por personajes de otros videojuegos, tanto de Nintendo como de otras desarrolladoras 
independientes, e incluye a todos los combatientes de entregas anteriores. Además, puede ampliarse con la compra de contenido descargable. 
Los trofeos coleccionables han sido reemplazados por los «espíritus», un nuevo concepto inspirado también en personajes de videojuegos, 
que permiten mejorar las características del luchador antes de un combate.2​ El título admite hasta ocho jugadores en modo local, se puede 
jugar en línea a través de Nintendo Switch Online, y es compatible con las figuras Amiibo y con Nintendo Labo.3​4​
<br> <br> 
El desarrollo de Ultimate comenzó en diciembre de 2015, bajo la dirección de Masahiro Sakurai y con buena parte del equipo que había 
hecho Super Smash Bros. para Nintendo 3DS y Wii U (2014), algo que redujo significativamente los tiempos de producción.5​ Sakurai logró 
su objetivo de incluir a todos los personajes que habían aparecido alguna vez en la entrega, un proceso complejo por el desarrollo técnico, 
por el equilibrio del plantel y por el coste de licencias.5​ Además de Nintendo, se llegaron a acuerdos con Bandai Namco, Konami, Sega, Capcom, 
quare Enix, PlatinumGames, Atlus, Microsoft, SNK, Disney y Mojang Studios.6​ Hay un total de 86 luchadores seleccionables —74 del plantel base y 
12 de contenido descargable— y 450 juegos representados entre combatientes, espíritus y trajes para el luchador Mii.7​
<br> <br> 
Ultimate es el tercer título más vendido en la historia de Nintendo Switch, con 24,77 millones de copias en formato físico y digital.8​ El título 
tuvo buenas reseñas de la prensa especializada, a pesar de algunas críticas relativas al multijugador en línea, y en términos generales ha sido 
considerado el mejor título de la saga Super Smash Bros. hasta la fecha. En 2019 fue premiado con el Game Awards al «mejor juego de lucha».9
